/**
 * @file    nt35510.c
 * @brief   NT35510 LCD 驱动实现 — FSMC 8080 16-bit 接口
 *
 * 指令集采用压缩表驱动: 每条记录 {cmd, data} 或 {sentinel, value}
 *   sentinel=0x0000 → END
 *   sentinel=0xFF00 → DELAY (value=微秒)
 *   sentinel=0xFF01 → CMD_ONLY (value=命令)
 */

#include "nt35510.h"
#include "main.h"
#include <string.h>

/* Sentinel 常量 */
#define NT35510_T_END     0x0000
#define NT35510_T_DELAY   0xFF00
#define NT35510_T_CMD     0xFF01

/* ================================================================
 * 内部辅助函数
 * ================================================================ */

/* 写 16-bit 命令到 CMD 寄存器 */
static inline void nt35510_write_cmd(uint16_t cmd)
{
    NT35510->CMD = cmd;
}

/* 写 16-bit 数据到 DAT 寄存器 */
static inline void nt35510_write_data(uint16_t data)
{
    NT35510->DAT = data;
}

/* us 级延时 (忙等, 仅 init 阶段使用; 168MHz ≈ 6ns/周期) */
static void nt35510_delay_us(uint32_t us)
{
    /* 粗略循环: 168MHz 下 ~168 周期/μs, 每轮 ~4 周期 → ~42 轮/μs */
    volatile uint32_t cnt = us * 42;
    while (cnt--) { __NOP(); }
}

