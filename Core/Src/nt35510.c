/* ================================================================
 * 内部状态
 * ================================================================ */
static uint8_t  g_dir    = NT35510_DIR_PORTRAIT;
static uint16_t g_width  = NT35510_WIDTH;   /* 当前方向有效宽度 */
static uint16_t g_height = NT35510_HEIGHT;  /* 当前方向有效高度 */

/* ================================================================
 * 公共 API
 * ================================================================ */

/**
 * @brief  执行初始化序列
 * @note   在 FreeRTOS 启动前调用 (HAL_Delay 可用)
 */
void NT35510_Init(void)
{
    uint32_t i;

    /* ===== 软件复位 (SWRESET) =====
     * NT35510 内部 POR 不可靠，必须显式发 0x0100 复位后再等 120ms。
     * 跳过此步骤可能导致寄存器处于不确定状态 → 随机花屏。
     * 参考: 知识库 F407_LCD_NT35510_GT5688芯片信息 第1.5节
     */
    nt35510_write_cmd(0x0100);          /* SWRESET */
    HAL_Delay(120);                     /* 等待复位完成 */

    /* 遍历初始化表 */
    for (i = 0; nt35510_init_seq[i] != NT35510_T_END; i += 2) {
        uint16_t a = nt35510_init_seq[i];
        uint16_t b = nt35510_init_seq[i + 1];

        if (a == NT35510_T_DELAY) {
            nt35510_delay_us(b);
        } else if (a == NT35510_T_CMD) {
            nt35510_write_cmd(b);
        } else {
            /* 常规寄存器写入: cmd → data */
            nt35510_write_cmd(a);
            nt35510_write_data(b);
        }
    }

    /* 设置为默认横屏方向 */
    NT35510_SetDir(NT35510_DIR_LANDSCAPE);
}

/**
 * @brief  设置显示方向
 * @param  dir: NT35510_DIR_PORTRAIT(竖屏480×800) / NT35510_DIR_LANDSCAPE(横屏800×480)
 */
void NT35510_SetDir(uint8_t dir)
{
    g_dir = dir;

    if (dir == NT35510_DIR_PORTRAIT) {
        /* 竖屏: 480×800 — 不交换 XY, 不翻转 */
        nt35510_write_cmd(0x3600);
        nt35510_write_data(0x0000);
        g_width  = NT35510_WIDTH;
        g_height = NT35510_HEIGHT;
    } else {
        /* 横屏正常朝向 0x00A0 (位5=MV,位6=MX,位7=MY)   0x0060=180°旋转 */
        nt35510_write_cmd(0x3600);
        nt35510_write_data(0x00A0);  /* 0x00A0: MV=1 MX=0 MY=0 BGR=0 -- landscape normal */
        g_width  = NT35510_HEIGHT;
        g_height = NT35510_WIDTH;
    }
}

/**
 * @brief  设置绘图窗口 (列/行地址)
 * @note   NT35510 使用拆分寄存器: 0x2A00~0x2A03 (X), 0x2B00~0x2B03 (Y)
 *         参数是逻辑坐标, 由方向决定物理映射
 */
void NT35510_SetWindow(uint16_t sx, uint16_t sy, uint16_t width, uint16_t height)
{
    /* X 范围: 起始→结束 */
    nt35510_write_cmd(0x2A00); nt35510_write_data(sx >> 8);
    nt35510_write_cmd(0x2A01); nt35510_write_data(sx & 0xFF);
    nt35510_write_cmd(0x2A02); nt35510_write_data(width >> 8);
    nt35510_write_cmd(0x2A03); nt35510_write_data(width & 0xFF);

    /* Y 范围: 起始→结束 */
    nt35510_write_cmd(0x2B00); nt35510_write_data(sy >> 8);
    nt35510_write_cmd(0x2B01); nt35510_write_data(sy & 0xFF);
    nt35510_write_cmd(0x2B02); nt35510_write_data(height >> 8);
    nt35510_write_cmd(0x2B03); nt35510_write_data(height & 0xFF);

    /* 内存写入命令 — 后续写 DAT 即为像素数据 */
    nt35510_write_cmd(0x2C00);
}

/**
 * @brief  快速写像素 (SetWindow 后连续调用来逐点填充)
 * @param  color: RGB565 颜色值
 */
void NT35510_WritePixel(uint16_t color)
{
    NT35510->DAT = color;
}

/**
 * @brief  画单个点 (含窗口设置)
 * @param  x, y: 逻辑坐标
 * @param  color: RGB565 颜色值
 */
void NT35510_DrawPixel(uint16_t x, uint16_t y, uint16_t color)
{
    NT35510_SetWindow(x, y, x, y);
    NT35510->DAT = color;
}

/**
 * @brief  读像素值 (调色/触摸反馈用)
 * @param  x, y: 逻辑坐标
 * @return RGB565 像素值
 */
uint16_t NT35510_ReadPixel(uint16_t x, uint16_t y)
{
    uint16_t r, g, b;

    NT35510_SetWindow(x, y, x, y);

    /* 0x2E00: 读内存命令 */
    nt35510_write_cmd(0x2E00);

    /* 第一次读是 dummy, 丢弃 */
    r = NT35510->DAT;

    /* 第二次读: R(高8位) + G(高8位) */
    r = NT35510->DAT;

    /* 第三次读: B(高8位) + 填充 */
    b = NT35510->DAT;

    /* 重组 RGB888 → RGB565 */
    g = r & 0x00FF;     /* G 在第一次读的低 8 位 */
    g <<= 8;
    r = r >> 11;        /* R 高 5 位 → bit[4:0] */
    g = g >> 10;        /* G 高 6 位 → bit[10:5] */
    b = b >> 11;        /* B 高 5 位 → bit[4:0] */

    return (r << 11) | (g << 5) | b;
}

/**
 * @brief  清屏 — 全屏填充单色
 * @param  color: RGB565 颜色值
 */
void NT35510_Clear(uint16_t color)
{
    /* 用 SetWindow 覆盖全屏, 然后连续写点 */
    NT35510_SetWindow(0, 0, g_width - 1, g_height - 1);

    uint32_t total = (uint32_t)g_width * g_height;
    for (uint32_t i = 0; i < total; i++) {
        NT35510->DAT = color;
    }
}

/**
 * @brief  矩形填充
 * @param  sx, sy: 左上角坐标
 * @param  ex, ey: 右下角坐标
 * @param  color: RGB565 颜色值
 */
void NT35510_Fill(uint16_t sx, uint16_t sy, uint16_t ex, uint16_t ey, uint16_t color)
{
    uint16_t w = ex - sx + 1;
    uint16_t h = ey - sy + 1;
    uint32_t total = (uint32_t)w * h;

    NT35510_SetWindow(sx, sy, ex, ey);

    for (uint32_t i = 0; i < total; i++) {
        NT35510->DAT = color;
    }
}

/**
 * @brief  获取当前方向有效宽度
 */
uint16_t NT35510_GetWidth(void)
{
    return g_width;
}

/**
 * @brief  获取当前方向有效高度
 */
uint16_t NT35510_GetHeight(void)
{
    return g_height;
}
